package BasicsMaven;

public class pojp_for_totalSum {
	private int bill_number;
	private int total_sum;
	public int getBill_number() {
		return bill_number;
	}
	public void setBill_number(int bill_number) {
		this.bill_number = bill_number;
	}
	public int getTotal_sum() {
		return total_sum;
	}
	public void setTotal_sum(int total_sum) {
		this.total_sum = total_sum;
	}
	@Override
	public String toString() {
		return "pojp_for_totalSum [bill_number=" + bill_number + ", total_sum=" + total_sum + "]";
	}
	
}
